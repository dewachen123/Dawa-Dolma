//switch statement 
//create a variable with a value from 1 to 7 

//use switch statement to log the day of the week based on number (1= monday,7=sunday)

function getDayOfWeek(number) {
  switch (number) {
    case 1:
      console.log("Monday");
      break;
    case 2:
      console.log("Tuesday");
      break;
    case 3:
      console.log("Wednesday");
      break;
    case 4:
      console.log("Thursday");
      break;
    case 5:
      console.log("Friday");
      break;
    case 6:
      console.log("Saturday");
      break;
    case 7:
      console.log("Sunday");
      break;
    
  }
}

getDayOfWeek(7);

